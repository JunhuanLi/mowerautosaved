void display_string_8x16(u8 fb,u16 page,u16 column,u8 *text)
{
	u16 i,j,k,n;
	
	CSB = 0;
	i = 0;
	
	while(text[i]>0x00)
	{
		if((text[i]>=0x20)&&(text[i]<=0x7e))
		{
			j=text[i]-0x20;

			for(n=0;n<2;n++)
			{
				lcd_address(page+n,column);
				for(k=0;k<8;k++)
				{
					if (fb==1) transfer_data(ascii_table_8x16[j][k+8*n]);
					else transfer_data(~(ascii_table_8x16[j][k+8*n]));
				}
			}
			i++;
			column += 8;
		}
		else
		i++;
	}
}


